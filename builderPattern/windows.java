package builderPattern;

public class windows implements operatingSystem{
    public void type(){
        System.out.println("Windows it is!!");
    }
    public void users() {
        System.out.println("There exists few users of windows phones");
    }
    public void version(){
        System.out.println("Currently it is running on latest version of Windows");
    }
    public void advantages(){
        System.out.println("It is an unique OS!!");
    }
    public void premium(){
        System.out.println("There are budget windows phones as well as premium phones too");
    }
}
