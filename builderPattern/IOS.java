package builderPattern;

public class IOS implements operatingSystem{
    public void type(){
        System.out.println("IOS it is!!");
    }
    public void users(){
        System.out.println("Available only on Apple devices");
    }
    public void version(){
        System.out.println("Currently it is running on latest version IOS 11");
    }
    public void advantages(){
        System.out.println("You get the best software integration in IOS! ");
    }
    public void premium(){
        System.out.println("IOS phones are available in premium category only!!");
    }
}
